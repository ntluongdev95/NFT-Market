export const blockchainAction =()=>async (dispatch) =>{
    dispatch({type:CONNECTION_REQUEST})
        if(window.ethereum){
            let web3 = new Web3(window.ethereum)
            try {
                const accounts = await window.ethereum.request({
                    method:"eth_accounts",
                });
                const contract_MM_Token =new web3.eth.Contract(abi_token,'0xd47e30F7FB67A460aea42e567CE778336Fea6D18')
                 const contract_MM_Market = new web3.eth.Contract(abi_market,'0x4C2EA1513599ca2239e58430984DA6B076E222C8')
                 const provider = new Web3.providers.WebsocketProvider('wss://rinkeby.infura.io/ws/v3/83f84b739a1e4155a36fd0df7411b05c')
                let web3_infura = new Web3(provider)
                 const contract_Infura = new web3_infura.eth.Contract(abi_market,'0x4C2EA1513599ca2239e58430984DA6B076E222C8')
                dispatch({
                    type:CONNECTION_SUCCESS,
                    payload:{
                        account:accounts[0],
                        contract_Infura:contract_Infura,
                        contract_MM_Token:contract_MM_Token,
                        contract_MM_Market:contract_MM_Market,
                        web3:web3
                    }
                })
                if(!accounts.length ){
                    toast.warn('Login to Metamask')
                }
                ;
                window.ethereum.on('accountsChanged',accounts=>{
                    dispatch(updateAccount(accounts[0]))
                })
                window.ethereum.on("chainChanged", () => {
                    window.location.reload();
                });

            } catch (error) {
                dispatch({
                    type:CONNECTION_FAILED,
                    payload:"Something went wrong"
                })
                toast.warn('Something went wrong')
            }
        }else{
            dispatch({
                type:CONNECTION_FAILED,
                payload:"You have not installed Metamask"
            })
            toast.warn("You have not installed Metamask")
        }
        
    
}




